class UserObserver
{
    /**
     * Handle the User "created" event.
     */
    public function created(User $user): void
    {
        // Assign the user the editor role if they don't have any roles.
        if (! $user->hasAnyRole(RolesEnum::cases())) {
            $user->assignRole(RolesEnum::EDITOR);
        }
    }

